add_filter('script_loader_tag', function($tag, $handle) {
    if (is_admin()) return $tag;

    $exclude = [
        'jquery',
        'jquery-core',
        'jquery-migrate',
        'wp-i18n',
        'wp-api-fetch',
        'wp-data',
        'wp-date',
        'wp-hooks',
        'moment'
    ];

    if (in_array($handle, $exclude)) {
        return $tag; // no defer
    }

    return str_replace(' src', ' defer src', $tag);
}, 10, 2);


